#include <stdio.h>

int main() {
    double input;

    printf("입력값: ");
    scanf("%lf", &input);

    /* [비트 변환 원리]
     * 1. Q8.8 Fixed-point 방식으로 하려면 정수부분, 소수부분 각각에 8비트를 할당해야 합니다.
     * 2. 따라서 입력값에 2^8(256)을 곱하면, 소수점 아래 8자리 비트들이 정수 위치로 올라옵니다.
     * 3. (short)형으로 변환하면 16비트 2의 보수 패턴이 완성됩니다.
     */
    short fixed_point = (short)(input * 256);

    printf("출력: ");

    // 16비트 전체를 MSB(최상위 비트)부터 하나씩 검사하며 출력합니다.
    for (int i = 15; i >= 0; i--) {
        /* * [비트 추출 원리: (fixed_point >> i) & 1]
        * 1. >> i : 검사하고 싶은 i번째 비트를 맨 오른쪽(LSB) 위치로 이동시킵니다.
        * 2. & 1  : i만큼 밀어버린 비트를 1과 AND 연산을 합니다. 조사하려는 비트를 제외한 나머지 상위 비트들을 가려주는 역할.
        * 3. 결과 : 제일 오른쪽(LSB)의 위치가 1이라면 1이 출력 0이라면 AND 연산으로 인해 0이 출력됩니다. 원래 i번째 비트가 1이었다면 '1 & 1'이 되어 1이 출력되고, 0이었다면 '0 & 1'이 되어 0이 출력됩니다.
        */
        printf("%d", (fixed_point >> i) & 1);

        // 상위 8비트(정수부) 출력 후 점(.)을 찍어줍니다.
        if (i == 8) {
            printf(".");
        }
    }
    printf("\n");

    return 0;
}
