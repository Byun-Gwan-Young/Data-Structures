#include <stdio.h>

// float와 unsigned int가 같은 4바이트 메모리를 공유하는 공용체
// 예) 5.75를 float로 저장하면 메모리에 01000000101110000000000000000000 이 들어감
//     scanner.f → IEEE 754 규칙으로 해석 → 5.75
//     scanner.i → 그냥 2진수로 해석    → 1083179008
// 비트는 똑같고 읽는 방식만 다름
// 우리가 원하는 건 5.75라는 값이 아니라 저장된 비트 자체이므로
// unsigned int로 읽어서 비트 연산(>>, &)을 사용할 수 있게 함
typedef union {
    float f;        // 입력받을 때 사용 (float로 저장)
    unsigned int i; // 비트 연산할 때 사용 (같은 메모리를 정수로 읽음)
} FloatScanner;

int main() {
    FloatScanner scanner;

    printf("입력: ");
    scanf("%f", &scanner.f);  // float로 입력받으면 같은 메모리에 비트 패턴이 저장됨

    printf("출력: ");

    // IEEE 754 단정밀도 32비트 구조
    // ┌─────┬──────────────────┬──────────────────────────────────────────────┐
    // │부호  │      지수         │                    가수                       │
    // │1bit │      8bit        │                    23bit                     │
    // │[31] │   [30] ~ [23]    │                 [22] ~ [0]                   │
    // └─────┴──────────────────┴──────────────────────────────────────────────┘
    // 31번 비트(MSB)부터 0번 비트(LSB)까지 순서대로 출력
    for (int i = 31; i >= 0; i--) {

        // 비트 추출 원리
        // scanner.i >> i  : i번째 비트를 맨 오른쪽(LSB)으로 이동
        // & 1             : 맨 오른쪽 비트만 남기고 나머지 제거 → 0 또는 1만 출력
        // 예) i=31일 때: 01000000101110000000000000000000 >> 31 = 0, 0 & 1 = 0 (부호비트)
        printf("%d", (scanner.i >> i) & 1);

        if (i == 31) {
            printf("/");        // 부호 비트(1자리) 출력 후 구분자
        } else if (i == 27) {
            printf(" ");        // 지수부를 4비트 단위로 나눔: 1000 0001
        } else if (i == 23) {
            printf("/");        // 지수 비트(8자리) 출력 후 구분자
        } else if (i < 23 && (22 - i) % 4 == 3 && i != 0) {
            printf(" ");        // 가수부를 4비트 단위로 나눔: 0111 0000 0000 ...
        }
    }

    printf("\n");
    return 0;
}
